// Arithmetic Operators

// Addition Operator
var a=1
var b=1
console.log("a + b =",a+b)

// Subtraction Operator
var a=1
var b=1
console.log("a - b =",a-b)

// Multiplication Operator
var a=5
var b=2
console.log("a * b =",a*b)

// Division Operator
var a=10
var b=2
console.log("a / b =",a/b)

// Modulus Operator
var a=200
var b=2
console.log("a % b =",a%b)

// Exponentiation Operator
var a=3
var b=2
console.log("a ** b =",a**b)

// Increment Operator
var a=10
var b=20
console.log("a++ =",a++)
console.log("a =",a)
console.log("++b =",++b)

// Decrement Operator
var c=10
var d=20
console.log("c-- =",c--)
console.log("c =",c)
console.log("--d =",--d)

//Assignment Operators
console.log("Assignment Operators")
var a=10
var b=20
var c=a+b
console.log("c =",c)

var a1=10
var b1=20
a1+=b1
console.log("a1 += b1 =",a1)